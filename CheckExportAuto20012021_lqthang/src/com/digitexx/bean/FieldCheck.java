package com.digitexx.bean;

public class FieldCheck {

	private  String columDb;
	private String columnFile;
	private String Rule;
	private String index;

	
	
	
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public String getRule() {
		return Rule;
	}
	public void setRule(String rule) {
		Rule = rule;
	}
	public String getColumDb() {
		return columDb;
	}
	public void setColumDb(String columDb) {
		this.columDb = columDb;
	}
	public String getColumnFile() {
		return columnFile;
	}
	public void setColumnFile(String columnFile) {
		this.columnFile = columnFile;
	}	
}
